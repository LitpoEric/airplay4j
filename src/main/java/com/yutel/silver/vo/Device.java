package com.yutel.silver.vo;

public class Device {
	private String deviceid;
	private String features;
	private String model;
	private String protovers;
	private String srcvers;

	public String getDeviceid() {
		return deviceid;
	}

	public void setDeviceid(String deviceid) {
		this.deviceid = deviceid;
	}

	public String getFeatures() {
		return features;
	}

	public void setFeatures(String features) {
		this.features = features;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getProtovers() {
		return protovers;
	}

	public void setProtovers(String protovers) {
		this.protovers = protovers;
	}

	public String getSrcvers() {
		return srcvers;
	}

	public void setSrcvers(String srcvers) {
		this.srcvers = srcvers;
	}
}
